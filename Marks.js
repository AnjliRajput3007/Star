
function marks(num){
  if(num >= 85){
    console.log("You passed exam with 1st Rank");
  } else if(num >= 65){
    console.log("You passed exam with 2nd Rank");
  } else if(num >= 36){
    console.log("You passed exam with 3rd Rank");
  } else {
    console.log("You failed the exam");
  }
}
marks(35);


